int	ft_str_is_printable(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '\0')
		return (1);
	while (str[i] >= ' ' && str[i] <= '~')
	{
		i++;
	}
	if (str[i] == '\0')
		return (1);
	else
		return (0);
}
