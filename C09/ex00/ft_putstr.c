#include <unistd.h>

void	ft_putstr(char *str)
{
	char	temp;

	temp = 0;
	while (1)
	{
		temp = *str;
		if (temp == '\0')
		{
			return ;
		}
		write (1, &temp, 1);
		str++;
	}
}
